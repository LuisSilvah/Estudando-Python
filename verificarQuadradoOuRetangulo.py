#  Ler dois numeros e verificar se é um Quadrado ou Retangulo

lado01 = float(input('Digite o valor do primeiro lado: '))
lado02 = float(input('Digite o valor do segundo lado: '))

if(lado01 == lado02):
    print(f"Os valores Digitados pertencem ao um quadrado")

else:
    print("Os valores Digitados pertencem ao um retângulo")    