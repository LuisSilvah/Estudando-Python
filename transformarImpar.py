# Transformar número em impar

numero = int(input('Digite um número inteiro: '))

if (numero % 2 == 0):
    numero = numero + 1
    print(f'Valor do Numero foi alterao para {numero}')

else: 
    print(f'Valor continua o mesmo: {numero}')

